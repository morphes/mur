<font color="#bc4600" size="3"><b>Case Study:  Automated Parking Management</b></font>









<p>A supplier of parking management solutions had been using helicopters to identify available spaces, flashing information regarding availability to a message board on the ground. 

<p>To automate the process and eliminate the helicopter cost, the company began installing inductive loop vehicle detectors at parking spots and using RFM SEMs (wireless Ethernet bridges) to transmit data to message boards. 

<p>The wireless link is necessary due to the high cost of cutting through parking lots to lay data cable; SEMs were chosen for their high-speed wireless Ethernet services and their ability to withstand harsh, outdoor environments. 




